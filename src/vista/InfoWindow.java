            ArrayList<String> genders = ConnectDB.getGender();
            for (String gender : genders) {
                jComboBox1.addItem(gender);
            }
            jComboBox1.addActionListener(new ActionListener(){
               @Override
                // Obtener el elemento seleccionado por el usuario
                public void actionPerformed(ActionEvent e){     
                    selectedGender = (String) jComboBox1.getSelectedItem();
            }}
            );
            ArrayList<String> idType = ConnectDB.getIdtype();
            for (String id : idType) {
                jComboBox4.addItem(id);
        }
            jComboBox4.addActionListener(new ActionListener(){
               @Override
                // Obtener el elemento seleccionado por el usuario
                public void actionPerformed(ActionEvent e){     
                    selectedidType = (String) jComboBox4.getSelectedItem();
            }}
            );
        
        
